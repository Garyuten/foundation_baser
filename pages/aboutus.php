<h1 id="AboutIndex">
	<span>baserCMSとは？</span></h1>
<div class="Section">
	<p>
		baserCMS（ベーサーシーエムエス）とは、オープンソースフレームワーク「<a href="http://cakephp.jp/" target="_blank">CakePHP</a>」をベースとし、環境準備の素早さに重点を置いた基本制作支援プロジェクトから生まれた国産CMS（コンテンツマネージメントシステム）です。</p>
	<p>
		WEBサイトに最低限必要となるメールフォームや新着ブログなどのプラグインや管理画面の枠組みを最初から装備し、携帯サイトにも対応しています。</p>
	<p>
		運用もカンタンで、パソコンに不慣れな方でも簡単に更新出来るような仕組みになっており、マニュアルやソースコードのコメントにおいて日本語を標準としているのも国産CMSの強みです。</p>
	<p>
		この「わかりやすい」baserCMSをWEB制作現場における制作の土台としてご利用頂けたら幸いです。</p>
