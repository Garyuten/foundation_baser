<h2 class="contentsHead">
	よくあるご質問</h2>
<h3 class="contentsHead">
	Q. baserCMSは無料ですか？</h3>
<p class="section">
	A. 無料です。<br />
	GPL ver2 ライセンスの範囲内であれば無料でご利用いただけます。<br />
	詳しくは「<a href="../license/index.html">ライセンスについて</a>」を参照ください。</p>
<h3 class="contentsHead">
	Q. ホームページを作った事がないですが設置できますか？</h3>
<p class="section">
	A. baserCMSには、デザインを簡単に変更できるデザインテーマの切り替え機能がありますが、現時点では、デザインテーマの充実ができておらず、一からデザインテーマを作る必要があります。<br />
	正直なところ基本的なホームページ知識がないと難しいです。<br />
	ただ、FTPでのアップロードと、アクセス権限の変更ができれば設置は可能です。<br />
	現時点でのbaserCMSは、管理画面上でデザインを簡単に変更できるようにはなっておらず、デザインの変更にはHTMLの知識が必須となります。 詳しくは、「<a href="../manuals/index.html">制作・開発マニュアル</a>」を参照ください。</p>
<h3 class="contentsHead">
	Q. CakePHPとはなんですか？</h3>
<p class="section">
	A. WEBアプリケーションフレームワークといって、WEBサイトにおけるプログラムの土台となるプログラムの集合体です。<br />
	家の建築に例えると基礎や出来上がったドアなどの部品のようなものです。<br />
	基礎がよく出来ていると家がしっかりとし、ドアやサッシなどの出来上がった部品があれば一から作るよりも工期が縮まります。<br />
	baserCMSは、CakePHPを土台とし、さらに、WEBサイトの土台を目指しています。</p>
<h3 class="contentsHead">
	Q. コーポレートサイトしか作る事ができないのですか？</h3>
<p class="section">
	A. そんな事はありません。<br />
	baserCMSはコーポレートサイトの制作経験をベースとして開発をすすめてきましたが、ポータルサイトへの組み込み実績もあります。<br />
	今後もコーポレートサイトをターゲットに最適化していく予定ですが、ポータルサイトなどへの土台としてもご利用できる事を前提としています。<br />
	baserCMSはあくまでWEBサイトの土台を目指します。</p>
<h3 class="contentsHead">
	Q. 制作が全然簡単ではないのですが？</h3>
<p class="section">
	A. 現時点(2009.12.07)ではそのとおりです。<br />
	baserCMSは今始まったばかりです。まだまだ機能的に未熟なところもたくさんあります。<br />
	これから皆様のフィードバックを頂きつつ、より扱いやすいCMSとなる事を目指しています。<br />
	ご提案、ご要望がございましたら、ぜひ<a href="http://forum.basercms.net/">ユーザーズフォーラム</a>にご参加ください。</p>
